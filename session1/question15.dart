// In Dart, being a statically-typed language means that the type of a variable is determined at compile-time, rather than at runtime. This means that when you declare a variable in Dart, its type (such as int, String, double, bool) must be specified or inferred, and once assigned, the variable's type cannot change during the execution of the program.