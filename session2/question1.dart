/*Create a string variable greeting with the value 'Hello, Dart!'.
 Print the length of greeting.
 Convert greeting to uppercase and print it.*/

void main() {
  String greeting = 'Hello, Dart!';
  print('Length of greeting: ${greeting.length}');
  String greetingUppercase = greeting.toUpperCase();
  print('Uppercase greeting: $greetingUppercase');
}
